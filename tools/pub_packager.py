def get_current_version(repo_root: Path) -> str:
    """Extract version from CITATION.cff."""
    cff = repo_root / "CITATION.cff"
    if cff.exists():
        for line in cff.read_text().splitlines():
            if line.startswith("version:"):
                return line.split(":", 1)[1].strip().strip('"')
    return "1.0.0"


def bump_version(version: str, bump: str = "patch") -> str:
    parts = version.lstrip("v").split(".")
    while len(parts) < 3:
        parts.append("0")
    major, minor, patch = int(parts[0]), int(parts[1]), int(parts[2])
    if bump == "major":
        return f"{major+1}.0.0"
    elif bump == "minor":
        return f"{major}.{minor+1}.0"
    else:
        return f"{major}.{minor}.{patch+1}"


def update_citation_cff(repo_root: Path, version: str, dry_run: bool) -> Path:
    """Update version and date-released in CITATION.cff."""
    aegis_check("cap.fs.write")
    cff_path = repo_root / "CITATION.cff"
    content = cff_path.read_text()
    today = datetime.now().strftime("%Y-%m-%d")
    content = re.sub(r'^version:.*$', f'version: {version}', content, flags=re.MULTILINE)
    content = re.sub(r'^date-released:.*$', f'date-released: {today}', content, flags=re.MULTILINE)
    if not dry_run:
        cff_path.write_text(content)
    print(f"  CITATION.cff: version={version} date={today}")
    return cff_path


def build_changelog(repo_root: Path, version: str, dry_run: bool) -> Path:
    """Generate CHANGELOG.md from recent git log."""
    aegis_check("cap.fs.write")
    changelog_path = repo_root / "CHANGELOG.md"

    try:
        log = subprocess.check_output(
            ["git", "log", "--oneline", "--no-merges", "-20"],
            cwd=repo_root, text=True, stderr=subprocess.DEVNULL
        )
        entries = [f"- {line.split(' ', 1)[1]}" for line in log.strip().splitlines() if ' ' in line]
    except Exception:
        entries = ["- See git log for full history"]

    today = datetime.now().strftime("%Y-%m-%d")
    new_section = f"## [{version}] — {today}\n\n" + "\n".join(entries[:15]) + "\n"

    if changelog_path.exists():
        existing = changelog_path.read_text()
        # Prepend after the first H1 heading if present
        if existing.startswith("# "):
            lines = existing.split("\n", 2)
            content = lines[0] + "\n\n" + new_section + ("\n" + lines[2] if len(lines) > 2 else "")
        else:
            content = f"# Changelog\n\n{new_section}{existing}"
    else:
        content = f"# Changelog\n\n{new_section}"

    if not dry_run:
        changelog_path.write_text(content)
    print(f"  CHANGELOG.md: {len(entries[:15])} entries for v{version}")
    return changelog_path


def build_release_bundle(repo_root: Path, version: str, dry_run: bool) -> Path:
    """Build a release_bundle.zip with papers + core docs."""
    aegis_check("cap.fs.write")
    bundle_path = repo_root / "output" / f"omega-architecture-v{version}.zip"
    bundle_path.parent.mkdir(exist_ok=True)

    if dry_run:
        print(f"  [DRY RUN] Would create: {bundle_path}")
        return bundle_path

    with zipfile.ZipFile(bundle_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for rel in CORE_PAPERS:
            p = repo_root / rel
            if p.exists():
                zf.write(p, rel)
                print(f"  + {rel}")
        for rel in BUNDLE_INCLUDES:
            p = repo_root / rel
            if p.exists():
                zf.write(p, rel)
                print(f"  + {rel}")
        # assets
        assets_dir = repo_root / "assets"
        if assets_dir.exists():
            for asset in assets_dir.iterdir():
                if asset.suffix in {".png", ".svg", ".pdf"}:
                    zf.write(asset, f"assets/{asset.name}")
                    print(f"  + assets/{asset.name}")

    size_kb = bundle_path.stat().st_size // 1024
    print(f"  Bundle: {bundle_path.name} ({size_kb} KB)")
    return bundle_path


def verify_bundle(bundle_path: Path, version: str) -> dict:
    """Verify bundle contains required artifacts."""
    required = ["papers/OmegA_Unified_Architecture_Paper.md", "CITATION.cff", "README.md"]
    if not bundle_path.exists():
        return {"valid": False, "missing": required, "reason": "bundle not created"}
    with zipfile.ZipFile(bundle_path) as zf:
        names = set(zf.namelist())
    missing = [r for r in required if r not in names]
    return {"valid": len(missing) == 0, "missing": missing, "files": len(names)}


def run_omegactl_audit(repo_root: Path) -> dict:
    """Run omegactl audit to verify spec compliance before release."""
    try:
        result = subprocess.run(
            ["python3", "omegactl.py", "audit"],
            cwd=repo_root, capture_output=True, text=True, timeout=60
        )
        passed = "PASS" in result.stdout and result.returncode == 0
        return {"passed": passed, "output": result.stdout[-500:]}
    except Exception as e:
        return {"passed": False, "output": str(e)}


